import json

def matrix_to_json(matrix, output_file):
    data = {
        "render_height": 1080.0,
        "render_width": 1920.0,
        "seconds": 2.0,
        "camera_path": [
            {
                "camera_to_world": [elem for row in matrix for elem in row],
                "fov": 75.0,
                "aspect": 1920.0 / 1080.0
            }
        ]
    }

    # Write the JSON data to a file
    with open(output_file, 'w') as file:
        json.dump(data, file, indent=4)

